
package com.dtag.bmp.service.aggregation.dto.types;


public class Customer {

    private String createdBy;
    private String createdDate;
    private Object customerCreditProfile;
    private Object customerRank;
    private Object customerStatus;
    private String customerType;
    private String id;
    private Object lastModificationDate;
    private Integer partyId;
    private String status;
    private String statusReason;
    private Object updatedBy;

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(String createdDate) {
        this.createdDate = createdDate;
    }

    public Object getCustomerCreditProfile() {
        return customerCreditProfile;
    }

    public void setCustomerCreditProfile(Object customerCreditProfile) {
        this.customerCreditProfile = customerCreditProfile;
    }

    public Object getCustomerRank() {
        return customerRank;
    }

    public void setCustomerRank(Object customerRank) {
        this.customerRank = customerRank;
    }

    public Object getCustomerStatus() {
        return customerStatus;
    }

    public void setCustomerStatus(Object customerStatus) {
        this.customerStatus = customerStatus;
    }

    public String getCustomerType() {
        return customerType;
    }

    public void setCustomerType(String customerType) {
        this.customerType = customerType;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Object getLastModificationDate() {
        return lastModificationDate;
    }

    public void setLastModificationDate(Object lastModificationDate) {
        this.lastModificationDate = lastModificationDate;
    }

    public Integer getPartyId() {
        return partyId;
    }

    public void setPartyId(Integer partyId) {
        this.partyId = partyId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatusReason() {
        return statusReason;
    }

    public void setStatusReason(String statusReason) {
        this.statusReason = statusReason;
    }

    public Object getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(Object updatedBy) {
        this.updatedBy = updatedBy;
    }

}
